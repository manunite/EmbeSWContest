void Oled::writeCmd(mraa::I2c* _i2c, uint8_t cmd)
{
   _i2c->writeReg(0x00, cmd);
}

 

void Oled::writeData(mraa::I2c* _i2c, uint8_t data)
{
   _i2c->writeReg(0x40, data);
}

 

void Oled::setPos(mraa::I2c* _i2c, uint8_t x, uint8_t y)
{
   this->writeCmd(_i2c, 0xb0 + y);
   this->writeCmd(_i2c, x & 0xf);
   this->writeCmd(_i2c, 0x10 | (x >> 4));
}

void Oled::fill(mraa::I2c* _i2c, uint8_t v)
{
   for (int i = 0; i < 8; i++)
   {
      this->writeCmd(_i2c, 0xb0 + i);
      this->writeCmd(_i2c, 0x00);
      this->writeCmd(_i2c, 0x10);

      for (int j = 0; j < 132; j++)
      {
         this->writeData(_i2c, v);
      }
   }
}

 

void Oled::cls(mraa::I2c* _i2c)
{
   this->fill(_i2c, 0x00);
}

 

void Oled::on(mraa::I2c* _i2c)
{
   this->writeCmd(_i2c, 0x80);
   this->writeCmd(_i2c, 0x14);
   this->writeCmd(_i2c, 0xaf);
}

void Oled::off(mraa::I2c* _i2c)
{
   this->writeCmd(_i2c, 0x8d);
   this->writeCmd(_i2c, 0x10);
   this->writeCmd(_i2c, 0xae);
}

